let map_token = function
  | JavaParser.TRUE (_, s)
  | JavaParser.FALSE (_, s)
  | JavaParser.NOT (_, s)
  | JavaParser.AND (_, s)
  | JavaParser.OR (_, s)
  | JavaParser.EQ (_, s)
  | JavaParser.METHOD (_, s)
  | JavaParser.IF (_, s)
  | JavaParser.ELSE (_, s)
  | JavaParser.WHILE (_, s)
  | JavaParser.NEW (_, s)
  | JavaParser.LPAREN (_, s)
  | JavaParser.RPAREN (_, s)
  | JavaParser.LBRACKET (_, s)
  | JavaParser.RBRACKET (_, s)
  | JavaParser.COMMA (_, s)
  | JavaParser.SEMICOLON (_, s)
  | JavaParser.DOT (_, s)
  | JavaParser.LEFT (_, s)
  | JavaParser.RIGHT (_, s)
  | JavaParser.AHEAD (_, s)
  | JavaParser.HERE (_, s)
  | JavaParser.NORTH (_, s)
  | JavaParser.SOUTH (_, s)
  | JavaParser.EAST (_, s)
  | JavaParser.WEST (_, s)
  | JavaParser.ID (s, _) -> s
  | JavaParser.INT (n, _) -> string_of_int n
  | JavaParser.EOF -> "EOF"
